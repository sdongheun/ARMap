    // Clustering Logic (Distance + Address Match)
    void ClusterAndBuildList(List<KakaoDocument> allPlaces)
    {
        _autoGeneratedBuildingList.Clear();
        HashSet<int> processedIndices = new HashSet<int>();

        for (int i = 0; i < allPlaces.Count; i++)
        {
            if (processedIndices.Contains(i)) continue;

            List<KakaoDocument> newCluster = new List<KakaoDocument>();
            KakaoDocument baseDoc = allPlaces[i];
            newCluster.Add(baseDoc);
            processedIndices.Add(i);

            for (int j = i + 1; j < allPlaces.Count; j++)
            {
                if (processedIndices.Contains(j)) continue;

                KakaoDocument compareDoc = allPlaces[j];

                double distance = HaversineDistance(double.Parse(baseDoc.y), double.Parse(baseDoc.x),
                    double.Parse(compareDoc.y), double.Parse(compareDoc.x));

                bool isSameAddress = NormalizeText(GetBestAddress(baseDoc)) == NormalizeText(GetBestAddress(compareDoc));

                if (distance <= clusterRadius && isSameAddress)
                {
                    newCluster.Add(compareDoc);
                    processedIndices.Add(j);
                }
            }
            _autoGeneratedBuildingList.Add(ProcessClusterIntoBuildingData(newCluster));
        }
    }

    // Convert API Data to BuildingData
    BuildingData ProcessClusterIntoBuildingData(List<KakaoDocument> cluster)
    {
        BuildingData newBuilding = new BuildingData();
        KakaoDocument representative = SelectRepresentativeDocument(cluster);

        newBuilding.buildingName = representative.place_name;
        newBuilding.latitude = double.Parse(representative.y);
        newBuilding.longitude = double.Parse(representative.x);
        newBuilding.altitude = 0;
        newBuilding.fetchedAddress = GetBestAddress(representative);
        newBuilding.description = representative.category_group_name;

        newBuilding.phoneNumber = representative.phone;
        newBuilding.placeUrl = representative.place_url;
        newBuilding.zipCode = representative.address_name;

        newBuilding.facilities = new List<FacilityInfo>();
        foreach (var doc in cluster)
        {
            FacilityInfo info = new FacilityInfo();
            info.name = doc.place_name;
            info.phone = doc.phone;
            info.category = doc.category_group_name;
            info.placeUrl = doc.place_url;

            newBuilding.facilities.Add(info);
        }

        return newBuilding;
    }

    KakaoDocument SelectRepresentativeDocument(List<KakaoDocument> cluster)
    {
        return cluster
            .OrderByDescending(GetDocumentQualityScore)
            .ThenByDescending(document => !string.IsNullOrWhiteSpace(document.road_address_name))
            .ThenByDescending(document => !string.IsNullOrWhiteSpace(document.phone))
            .ThenByDescending(document => document.place_name?.Length ?? 0)
            .First();
    }

    void CreateAllBuildingAnchors()
    {
        foreach (var anchor in _buildingAnchors.Values) if (anchor != null) Destroy(anchor.gameObject);
        _buildingAnchors.Clear();
        int anchorFailureCount = 0;

        foreach (var building in _autoGeneratedBuildingList)
        {
            string buildingKey = GetBuildingAnchorKey(building);
            ARGeospatialAnchor anchor = AnchorManager.AddAnchor(building.latitude, building.longitude, building.altitude, Quaternion.identity);

            if (anchor != null)
            {
                _buildingAnchors[buildingKey] = anchor;

                if (buildingMarkerPrefab != null)
                {
                    GameObject markerObj = Instantiate(buildingMarkerPrefab, anchor.transform);
                    markerObj.transform.localPosition = Vector3.up * GetDynamicMarkerHeightOffset(anchor.transform.position);

                    BuildingMarker marker = markerObj.GetComponent<BuildingMarker>();
                    if (marker != null)
                    {
                        marker.SetState(BuildingMarker.MarkerVisualState.Hidden, true);
                    }
                }
            }
            else
            {
                anchorFailureCount++;
                Debug.LogWarning($"Failed to create geospatial anchor for {building.buildingName} ({buildingKey}).");
            }
        }

        if (anchorFailureCount > 0)
        {
            arUIManager?.ShowToast("일부 건물 마커를 표시하지 못했습니다.");
        }
    }
    //건물 인식 방식 결정
    BuildingData FindBestTargetFromExistingAnchors(List<VisibleBuildingCandidate> visibleCandidates) // 카메라 시점과 위치를 기반으로 가장 적합한 건물 감지
    {
        float pitchAngle = 90.0f - Vector3.Angle(_cameraTransform.forward, Vector3.up);
        if (Mathf.Abs(pitchAngle) > verticalAngleLimit) return null;

        List<VisibleBuildingCandidate> frontMostCandidates = GetFrontMostCandidates(visibleCandidates);
        BuildingData bestTarget = null;
        BuildingData fallbackTarget = null;
        float bestScore = float.MaxValue;
        float fallbackScore = float.MaxValue;
        Vector2 viewportCenter = new Vector2(0.5f, 0.5f);
        float relaxedViewportThreshold = Mathf.Min(0.38f, centerViewportThreshold + 0.1f);

        foreach (VisibleBuildingCandidate candidate in frontMostCandidates)
        {
            float centerDistance = Vector2.Distance(new Vector2(candidate.viewportPoint.x, candidate.viewportPoint.y), viewportCenter);
            float score = centerDistance + candidate.distance * 0.0015f;

            if (centerDistance <= centerViewportThreshold && score < bestScore)
            {
                bestScore = score;
                bestTarget = candidate.building;
            }

            if (centerDistance <= relaxedViewportThreshold && score < fallbackScore)
            {
                fallbackScore = score;
                fallbackTarget = candidate.building;
            }
        }

        return bestTarget ?? fallbackTarget;
    }

    BuildingData FindBestTargetForQuickInfoFallback(List<VisibleBuildingCandidate> visibleCandidates)
    {
        if (visibleCandidates == null || visibleCandidates.Count == 0)
        {
            return null;
        }

        Vector2 viewportCenter = new Vector2(0.5f, 0.5f);
        VisibleBuildingCandidate bestCandidate = null;
        float bestScore = float.MaxValue;

        foreach (VisibleBuildingCandidate candidate in visibleCandidates)
        {
            float centerDistance = Vector2.Distance(new Vector2(candidate.viewportPoint.x, candidate.viewportPoint.y), viewportCenter);
            float score = centerDistance + candidate.distance * 0.002f;
            if (score < bestScore)
            {
                bestScore = score;
                bestCandidate = candidate;
            }
        }

        return bestCandidate?.building;
    }

    List<VisibleBuildingCandidate> GetVisibleBuildingCandidates()
    {
        List<VisibleBuildingCandidate> visibleBuildings = new List<VisibleBuildingCandidate>();

        foreach (var building in _autoGeneratedBuildingList)
        {
            string buildingKey = GetBuildingAnchorKey(building);
            if (!_buildingAnchors.ContainsKey(buildingKey))
            {
                continue;
            }

            ARGeospatialAnchor anchor = _buildingAnchors[buildingKey];
            if (anchor == null)
            {
                continue;
            }

            Vector3 markerWorldPosition = GetScreenMarkerWorldPosition(anchor);
            float distance;
            Vector3 viewportPoint;

            if (!TryGetVisibleViewportPoint(markerWorldPosition, out viewportPoint, out distance))
            {
                continue;
            }

            if (distance > anchorPreviewRadius)
            {
                continue;
            }

            visibleBuildings.Add(new VisibleBuildingCandidate
            {
                building = building,
                buildingKey = buildingKey,
                anchor = anchor,
                viewportPoint = viewportPoint,
                distance = distance
            });
        }

        return visibleBuildings;
    }

    List<VisibleBuildingCandidate> GetFrontMostCandidates(List<VisibleBuildingCandidate> visibleCandidates)
    {
        List<VisibleBuildingCandidate> sortedCandidates = visibleCandidates
            .Where(candidate => candidate.distance <= detectionRadius)
            .OrderBy(candidate => candidate.distance)
            .ToList();

        List<VisibleBuildingCandidate> frontMostCandidates = new List<VisibleBuildingCandidate>();

        foreach (VisibleBuildingCandidate candidate in sortedCandidates)
        {
            Vector2 candidateViewport = new Vector2(candidate.viewportPoint.x, candidate.viewportPoint.y);
            bool isRearCandidate = frontMostCandidates.Any(existing =>
            {
                Vector2 existingViewport = new Vector2(existing.viewportPoint.x, existing.viewportPoint.y);
                float viewportDistance = Vector2.Distance(candidateViewport, existingViewport);
                return viewportDistance <= forwardGroupViewportThreshold &&
                       candidate.distance >= existing.distance + groupedCandidateDistanceBias;
            });

            if (!isRearCandidate)
            {
                frontMostCandidates.Add(candidate);
            }
        }

        return frontMostCandidates;
    }

    bool TryGetVisibleViewportPoint(Vector3 worldPosition, out Vector3 viewportPoint, out float distance)
    {
        viewportPoint = Camera.main.WorldToViewportPoint(worldPosition);
        distance = Vector3.Distance(_cameraTransform.position, worldPosition);

        if (viewportPoint.z <= 0f)
        {
            return false;
        }

        if (viewportPoint.x < 0f || viewportPoint.x > 1f || viewportPoint.y < 0f || viewportPoint.y > 1f)
        {
            return false;
        }

        return true;
    }

    void UpdateWorldSpaceInfoMarker(BuildingData selectedBuilding)
    {
        if (!showWorldSpaceInfoMarker || selectedBuilding == null)
        {
            return;
        }

        string buildingKey = GetBuildingAnchorKey(selectedBuilding);
        if (!_buildingAnchors.ContainsKey(buildingKey))
        {
            return;
        }

        ARGeospatialAnchor anchor = _buildingAnchors[buildingKey];
        if (anchor == null)
        {
            return;
        }

        BuildingMarker marker = anchor.GetComponentInChildren<BuildingMarker>();
        if (marker == null)
        {
            return;
        }

        float distance = Vector3.Distance(_cameraTransform.position, anchor.transform.position);
        marker.SetInfoContent(selectedBuilding.buildingName, GetMarkerSubtitle(selectedBuilding, distance));
        marker.SetInfoWorldPosition(GetInfoMarkerWorldPosition(anchor));
        marker.SetInfoVisible(true);
    }

    Vector3 GetInfoMarkerWorldPosition(ARGeospatialAnchor anchor)
    {
        Vector3 cameraPosition = _cameraTransform.position;
        Vector3 anchorPosition = GetMarkerWorldPosition(anchor);
        Vector3 toAnchor = anchorPosition - cameraPosition;
        float anchorDistance = toAnchor.magnitude;

        if (anchorDistance <= Mathf.Epsilon)
        {
            return anchorPosition;
        }

        float targetDistance = Mathf.Clamp(anchorDistance * infoMarkerLerp, infoMarkerMinDistance, infoMarkerMaxDistance);
        return cameraPosition + toAnchor.normalized * targetDistance;
    }
    void CheckMovementAndReload() // 위치 변경에 따른 데이터 새로고침 여부 판단
    {
        if (_isReloadingData)
        {
            return;
        }

        if (Input.location.status == LocationServiceStatus.Running && _isDataLoaded)
        {
            LocationInfo currentLoc = Input.location.lastData;
            double distance = HaversineDistance(_lastLoadedLocation.latitude, _lastLoadedLocation.longitude, currentLoc.latitude, currentLoc.longitude);
            if (distance >= refreshDistance) { StartCoroutine(ReloadDataAtCurrentLocation()); }
        }
    }
    IEnumerator ReloadDataAtCurrentLocation() // 현재 위치에서 데이터 새로고침
    {
        if (_isReloadingData)
        {
            yield break;
        }

        _isReloadingData = true;
        LocationInfo currentLoc = Input.location.lastData;
        _isDataLoaded = false;
        Debug.Log($"Reloading nearby place data. Lat: {currentLoc.latitude}, Lon: {currentLoc.longitude}");
        yield return StartCoroutine(FetchAndClusterNearbyPlaces(currentLoc.latitude, currentLoc.longitude));
        _lastLoadedLocation = currentLoc;
        _isDataLoaded = true;
        _isReloadingData = false;
    }
    private double HaversineDistance(double lat1, double lon1, double lat2, double lon2)
    // 두 지점 간의 거리를 계산하는 Haversine 공식
    {
        const double R = 6371e3; var phi1 = lat1 * (Math.PI / 180.0);
        var phi2 = lat2 * (Math.PI / 180.0); var deltaPhi = (lat2 - lat1) * (Math.PI / 180.0);
        var deltaLambda = (lon2 - lon1) * (Math.PI / 180.0);
        var a = Math.Sin(deltaPhi / 2) * Math.Sin(deltaPhi / 2) + Math.Cos(phi1) * Math.Cos(phi2) * Math.Sin(deltaLambda / 2) * Math.Sin(deltaLambda / 2);
        var c = 2 * Math.Atan2(Math.Sqrt(a), Math.Sqrt(1 - a)); return R * c;
    }
